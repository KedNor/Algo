function binaireToText(chiffre) {
    chiffre = chiffre.split(" ");//on separe les characters
    let puissance;
    let decimal = 0;
    let transfoChaine = "";
  
    // boucle pour vérifier chaque nombre binaire du tableau
    for (let i = 0; i < chiffre.length; i++) {
      
      for (let j = 0; j < chiffre[i].length; j++) {
       
        if (chiffre[i][j] == 1) {
          
          puissance = Math.pow(2, +chiffre[i].length - j - 1);
          decimal += puissance;
  
        
        }
      }
  
      // convertir notre binaire en décimal et le convertir en String
      transfoChaine += String.fromCharCode(decimal);
  
      // reset
      decimal = 0;
    }
  
    return transfoChaine;
  }
  
